/**
 * Adds two numbers
 * @param {number} a - First number
 * @param {number} b - Second number
 * @returns {number} Sum of a and b
 */
const add = (a, b) => a + b;

/**
 * Greets a user
 * @param {string} name - Name of the user
 * @returns {string} Greeting message
 */
const greet = (name) => `Hello, ${name}!`;

export { add, greet };
